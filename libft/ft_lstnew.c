#include "libft.h"

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list *list;

	if (!(list = (t_list *)malloc(sizeof(*list))))
		return (NULL);
	if (!content)
	{
		list->content = NULL;
		list->content_size = 0;
	}
	else
	{
		if (!(list->content = malloc(1)))
			return (NULL);
		ft_memcpy(list->content, content, 1);
		list->content_size = content_size;
	}
	list->next = NULL;
	return (list);
}
